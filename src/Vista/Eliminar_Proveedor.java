    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaInicio = Datos_Proveedor.getSelectedRow();
        int numFS = Datos_Proveedor.getSelectedRowCount();
        ArrayList<String> listIdProveedor = new ArrayList();
        String idProveedor = "";
        if (filaInicio >= 0) {
            for (int i = 0; i < numFS; i++) {
                idProveedor = String.valueOf(Datos_Proveedor.getValueAt(i + filaInicio, 0));
                listIdProveedor.add(idProveedor);
            }
            System.out.println(listIdProveedor);

            for (int i = 0; i < numFS; i++) {
                int rptaUsuario = JOptionPane.showConfirmDialog(null, "Â¿Quiere eliminar el registro con id=" + idProveedor + "?");
                if (rptaUsuario == 0) {
                    proveedor.eliminarProveedor(Integer.parseInt(idProveedor));
                }
            }
            LlenarTabla(Datos_Proveedor);
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar solo una fila");
        }
        btnEliminar.setEnabled(false);
        btnListar.setEnabled(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

